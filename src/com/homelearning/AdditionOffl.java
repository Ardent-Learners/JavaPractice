package com.homelearning;

public class AdditionOffl {

    public int add(int[] array) {
        return array[0] + array[array.length - 1] ;
    }


    public static void main(String[] args){
        AdditionOffl ad = new AdditionOffl();
        ad.add(new int[]{1,2,3,4,5,});



    }
}








































